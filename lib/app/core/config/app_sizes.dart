/// Defines commonly used sizes, paddings, and radii for the app UI.
/// This class centralizes all size-related constants to ensure consistent
/// spacing and dimensions across the application's user interface.
class AppSizes {
  // Padding values for consistent spacing
  /// Horizontal padding for pages.
  /// Used for consistent left and right margins on all pages.
  static const double pageHorizontalPadding = 16.0;

  /// Vertical padding for pages.
  /// Used for consistent top and bottom margins on all pages.
  static const double pageVerticaladding = 40.0;

  /// Padding for splash screens.
  /// Used for the splash screen layout to ensure proper spacing.
  static const double splashPadding = 32.0;

  /// Vertical padding for buttons.
  /// Used for consistent button height and touch target size.
  static const double verticalButtonPadding = 12.0;

  /// Spacing between content elements.
  /// Used for consistent gaps between UI components.
  static const double contentSpacing = 12.0;

  // Border radius values for rounded corners
  /// Card border radius.
  /// Used for cards, containers, and elevated surfaces.
  static const double cardRadius = 12.0;

  /// Button border radius.
  /// Used for buttons and interactive elements.
  static const double buttonRadius = 16.0;

  // Height and width values for consistent component dimensions
  /// Height of the app bar.
  /// Standard height for all app bars in the application.
  static const double appBarHeight = 56.0;

  /// Height of the bottom navigation bar.
  /// Standard height for the bottom navigation component.
  static const double bottomNavBarHeight = 72.0;

  /// Width of the splash logo.
  /// Specific width for the splash screen logo display.
  static const double splashLogoWidth = 249.35;

  /// Height of buttons.
  /// Standard height for all buttons to ensure consistent touch targets.
  static const double buttonHeight = 56.0;

  // Icon dimensions for consistent icon sizing
  /// Default icon size.
  /// Standard size for icons throughout the application.
  static const double iconSize = 24.0;
}
